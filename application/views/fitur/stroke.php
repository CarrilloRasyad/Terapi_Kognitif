        <h2>Pengertian</h2>
        <p>Stroke adalah kondisi yang terjadi ketika pasokan darah ke otak mengalami gangguan atau berkurang akibat penyumbatan (stroke iskemik) atau pecahnya pembuluh darah (stroke hemoragik). Tanpa pasokan darah, otak tidak akan mendapatkan asupan oksigen dan nutrisi, sehingga sel-sel pada sebagian area otak akan mati. Kondisi ini menyebabkan bagian tubuh yang dikendalikan oleh area otak yang rusak tidak dapat berfungsi dengan baik. Stroke merupakan kondisi gawat darurat yang perlu ditangani secepatnya, karena sel otak dapat mati hanya dalam hitungan menit. Tindakan penanganan yang cepat dan tepat dapat meminimalkan tingkat kerusakan otak dan mencegah kemungkinan munculnya komplikasi.</p>
        <h2>Gejala</h2>
		<p class="mb-1">Setidaknya, ada tiga gejala utama stroke yang mudah untuk dikenali, yaitu:</p>
		  <ul>
             <li>Salah satu sisi wajah akan terlihat lebih turun dan pengidap tidak mampu tersenyum karena mulut atau mata tampak terkulai.</li>
             <li>Pengidap tidak mampu mengangkat salah satu lengan karena terasa lemas atau mati rasa. Tidak hanya lengan, tungkai yang berada pada sisi yang sama dengan lengan juga mengalami kelemahan.</li>
             <li>Ucapan menjadi tidak jelas, kacau, atau bahkan tidak mampu berbicara sama sekali meski pengidap terlihat sadar.</li>
			 <li>Mempelajari informasi baru</li>
          </ul>
		<p class="mb-1">Sementara itu, gejala dan tanda stroke lainnya adalah:</p>
          <ul>
			 <li>Mual dan muntah.</li>
			 <li>Sakit kepala hebat yang datang secara tiba-tiba, disertai kaku pada leher dan pusing seperti berputar (vertigo).</li>
			 <li>Mengalami penurunan kesadaran.</li>
			 <li>Sulit menelan (disfagia) sehingga mengakibatkan tersedak.</li>
			 <li>Mengalami gangguan pada keseimbangan dan koordinasi.</li>
			 <li>Mengalami hilang penglihatan secara tiba-tiba atau penglihatan ganda.</li>
		  </ul>

        <h2>Penyebab</h2>
        <p class="mb-1">Berdasarkan penyebabnya, stroke terbagi menjadi dua jenis, yaitu:</p>
          <ul>
             <li>Stroke iskemik</li>
          </ul>
        <p><span>Terjadi ketika pembuluh darah arteri yang membawa darah dan oksigen ke otak mengalami penyempitan, sehingga menyebabkan aliran darah ke otak sangat berkurang. Kondisi ini disebut juga dengan iskemia. Stroke iskemik dapat dibagi lagi ke dalam 2 jenis, stroke trombotik dan stroke embolik.</span></p>
          <ul>
             <li>Stroke hemoragik</li>
          </ul>
        <p><span>Terjadi ketika pembuluh darah di otak pecah sehingga menyebabkan perdarahan. Pendarahan di otak dapat dipicu oleh beberapa kondisi yang memengaruhi pembuluh darah. Misalnya hipertensi yang tidak terkendali, dinding pembuluh darah yang lemah, dan sedang menjalani pengobatan dengan pengencer darah. Stroke hemoragik terbagi lagi menjadi dua jenis, yaitu perdarahan intraserebral dan subarachnoid.</span></p>
		<h2>Penanganan</h2>
        <p><span\>Pengobatan khusus yang diberikan pada pengidap stroke bergantung pada jenis stroke yang dialaminya, apakah mengarah pada stroke iskemik atau stroke hemoragik.</span></p>
          <ul>
             <li>Pengobatan stroke iskemik</li>
          </ul>
        <p><span>Penanganan awal akan berfokus untuk menjaga jalan napas, mengontrol tekanan darah, dan mengembalikan aliran darah.</span></p>
          <ul>
             <li>Pengobatan stroke hemoragik</li>
          </ul>
        <p><span>Sementara pada kasus stroke hemoragik, pengobatan awal bertujuan untuk mengurangi tekanan pada otak dan mengontrol perdarahan. Ada beberapa bentuk pengobatan yang dilakukan, antara lain konsumsi obat-obatan dan operasi.</span></p>
          <ul>
             <li>Pengobatan TIA</li>
          </ul>
        <p><span>Pengobatan TIA bertujuan untuk menurunkan faktor risiko yang dapat memicu timbulnya stroke, sehingga penyakit jantung dapat dicegah. Dalam beberapa kasus, prosedur operasi endarterektomi karotis diperlukan jika terdapat penumpukan lemak pada arteri karotis.</span></p>
