        <h2>Pengertian</h2>
        <p>Afasia adalah gangguan otak yang dapat memengaruhi keterampilan berbicara dan menulis seseorang. Ini dapat menyebabkan penderita kehilangan kemampuan untuk berbahasa dan memahami orang lain dengan baik. Ini terjadi saat bagian otak yang mengandung pemahaman berbahasa mengalami kerusakan (biasanya akibat <a href="<?= base_url('fitur/stroke')?>" >stroke</a>). Orang dengan afasia sering kali susah payah menemukan kata yang tepat digunakan. Mereka cenderung menggunakan kata-kata yang salah dan mengubah pengucapannya. Contohnya, mereka akan mengatakan, “piring pencuci”, padahal yang dimaksud adalah “pencuci piring.” Karena mereka tidak melakukannya dengan sengaja (bahkan tidak menyadarinya), mereka dapat merasa frustrasi ketika orang lain tidak memahami apa yang berusaha mereka ungkapkan. Gangguan ini juga dapat memengaruhi keterampilan mendengar dan membaca. Namun, ini tidak berdampak apa pun terhadap kecerdasan.</p>
        <h2>Gejala</h2>
		<p class="mb-1">Pasien afasia kemungkinan sulit untuk:</p>
		  <ul>
             <li>Fokus dengan apa yang dia katakan.</li>
             <li>Memberi atau mengikuti petunjuk.</li>
             <li>Mengartikan bahasa abstrak.</li>
			 <li>Mempelajari informasi baru.</li>
			 <li>Memproses informasi visual.</li>
			 <li>Mengingat kenangan.</li>
			 <li>Mengenali dan membuat pemecahan masalah.</li>
			 <li>Tetap berada dalam satu topik dalam suatu percakapan.</li>
			 <li>Menceritakan peristiwa secara berurutan.</li>
			 <li>Memahami isyarat non-verbal.</li>
			 <li>Menggunakaan ekspresi wajah yang tepat.</li>
			 <li>Mengingat kata-kata tertentu.</li>
			 <li>Membaca atau menulis dengan benar.</li>
			 <li>Mengungkapkan kalimat yang lengkap, sebagian besar hanya memakai kata benda dan kata kerja.</li>
			 <li>Memakai kata-kata yang tepat, pasien sering kali memakai kata-kata yang salah.</li>
		  </ul>

        <h2>Penyebab</h2>
        <p>Gangguan ini dapat terjadi saat bagian otak yang mengatur kemampuan berbahasa, mengalami kerusakan akibat cedera atau penyakit. Ini termasuk stroke, tumor otak, dan cedera otak traumatik. Afasia dapat berkembang secara bertahap pada orang yang menderita gangguan saraf progresif.</p>
		<p>Pengobatan awal untuk afasia berfokus pada mengobati penyakit yang mendasarinya. Contohnya, pasien stroke diobati dengan tujuan melancarkan aliran darah menuju otak. Sedangkan pasien kanker otak kemungkinan perlu menjalani operasi untuk mengangkat tumor pada otak. Terapi wicara dan bahasa dapat dimulai tepat setelah prosedur tersebut.</p>
		<p>Ada dua jenis afasia, yaitu lancar dan tidak lancar. Orang dengan afasia lancar dapat menyusun kalimat yang cukup panjang, namun sering kali dengan kata yang salah. Di sisi lain, orang yang menderita afasia tidak lancar berusaha keras untuk mengungkapkan kata-kata dan mengucapkannya dalam kalimat yang sangat pendek. Contohnya, mungkin hanya mengatakan, “mau keju” atau “pergi restoran.”</p>
        
		<h2>Penanganan</h2>
        <p>Perawatan untuk afasia biasanya melibatkan terapi wicara, yang dapat membantu meningkatkan keterampilan bahasa dan kemampuan komunikasi. Perawatan lain mungkin termasuk pengobatan atau pembedahan, tergantung pada penyebab yang mendasari kondisi tersebut.</p>
